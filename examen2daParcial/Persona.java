
package com.mycompany.examen2;

public class Persona {
    private String nombre;
    private int edad;
    private float perosPersona;

    public Persona(String nombre, int edad, float perosPersona) {
        this.nombre = nombre;
        this.edad = edad;
        this.perosPersona = perosPersona;
    }

    public String getNombre() {
        return nombre;
    }

    public int getEdad() {
        return edad;
    }

    public float getPerosPersona() {
        return perosPersona;
    }
    
    
}
